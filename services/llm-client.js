        switch (agentCode) {
            case 'violation':
                return this.mockViolation(msg, userMsg, hasContext ? contextMsg.content : '');
            case 'hr':
                return this.mockHR(msg, userMsg, hasContext ? contextMsg.content : '');
            case 'task':
                return this.mockTask(msg, userMsg, hasContext ? contextMsg.content : '');
            case 'compliance':
                return this.mockCompliance(msg, userMsg, hasContext ? contextMsg.content : '');
            case 'production':
                return this.mockProduction(msg, userMsg, hasContext ? contextMsg.content : '');
            default:
                return '您好！我是AI智能助手，请问有什么可以帮您的？';
        }
